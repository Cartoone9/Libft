int	ft_toupper(int c)
{
	unsigned char	c_uc;

	if (c == -1)
		return (-1);
	c_uc = (unsigned char)c;
	if (c_uc >= 97 && c_uc <= 122)
		c_uc -= 32;
	return ((int)c_uc);
}
