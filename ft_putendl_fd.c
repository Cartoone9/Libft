#include "libft.h"

void	ft_putendl_fd(char *s, int fd)
{
	size_t	s_len;

	if (!s || fd == -1)
		return ;
	s_len = ft_strlen((const char *)s);
	write(fd, s, s_len);
	write(fd, "\n", 1);
}
