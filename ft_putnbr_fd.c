#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	n_ln;

	if (fd == -1)
		return ;
	n_ln = (long)n;
	if (n_ln < 0)
	{
		ft_putchar_fd('-', fd);
		n_ln = -(n_ln);
	}
	if (n_ln >= 10)
		ft_putnbr_fd((int)(n_ln / 10), fd);
	ft_putchar_fd((char)((n_ln % 10) + '0'), fd);
}
