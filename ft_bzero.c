#include "libft.h"

void	ft_bzero(void *ptr, size_t n)
{
	size_t			i;
	unsigned char	*ptr_uc;

	i = 0;
	ptr_uc = (unsigned char *)ptr;
	while (i < n)
	{
		ptr_uc[i] = 0;
		i++;
	}
}
